<?php

namespace app\models;

use Yii;
use yii\behaviors\TimestampBehavior;

/**
 * This is the model class for table "order".
 *
 * @property int $id
 * @property int $total
 * @property int $user_id
 * @property int $created_at
 * @property int $updated_at
 *
 * @property Basket[] $baskets
